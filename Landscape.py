def drawtree():
  tree.showturtle()
  tree.color("green")
  tree.speed(100)
  tree.pu()
  tree.setpos(-100, -180)
  tree.seth(90)
  tree.pd()
  def draw(l):
    if(l<10):
      return
    else:
      tree.fd(l)
      tree.lt(30)
      draw(3*l/4)
      tree.rt(60)
      draw(3*l/4)
      tree.lt(30)
      tree.backward(l)
  draw(100)
  tree.hideturtle()

def drawsun():
  sun.showturtle()
  sun.speed(100)
  sun.pu()
  sun.setpos(-100, 200)
  sun.color("orange")
  sun.pd()
  sun.begin_fill()
  sun.circle(50,360)
  sun.end_fill()
  for i in range(18):
    sun.right(90)
    sun.penup()
    sun.forward(10)
    sun.pendown()
    sun.forward(10)
    sun.penup()
    sun.backward(20)
    sun.left(90)
    sun.circle(50,20)
  sun.hideturtle()
    



