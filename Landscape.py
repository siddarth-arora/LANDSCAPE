def drawsky():
  sky.showturtle()
  sky.speed(100)
  sky.color('#e5e5ff')
  sky.pensize(100)
  sky.setpos(-300, -200)
  for i in skycol:
     temp = '#' + i
     sky.color(temp)
     sky.forward(1100)
     sky.left(90)
     sky.forward(25)
     sky.left(90)

     sky.color(temp)
     sky.forward(1100)
     sky.right(90)
     sky.forward(25)
     sky.right(90)
  sky.hideturtle()

